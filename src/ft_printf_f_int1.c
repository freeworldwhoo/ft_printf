#include "libft.h"

void	ft_add_int(size_t **a, size_t *b, unsigned int l)
{
	size_t			k;
	unsigned int	i;

	i = 0;
	k = 0;
	while (i < l)
	{
		(*a)[l - 1 - i] = (*a)[l - 1 - i] + b[l - 1 - i] + k;
		k = (*a)[l - 1 - i] / 1000000000000000000;
		(*a)[l - 1 - i++] -= 1000000000000000000 * k;
	}
}
